                                       function hideNext() { arasjQuery('.pagination .pagination-next').hide(); }
                                       function showNext() { arasjQuery('.pagination .pagination-next').show(); }
                                       
                                       function hidePagination() { arasjQuery('.pagination').hide(); }
                                       function hidePosts() { arasjQuery('#posts').hide(700); }
                                       function hideCount() { arasjQuery('#display-count').hide(700); }
                                       
                                       function hideAlert() { arasjQuery('#alert-info').hide(1000); }
                                       function showAlert() { arasjQuery('#alert-info').show(1000); }
                                    
                                       function paginate(page,pageCount) {
                                           var source = arasjQuery("#pagination-template").html();
                                           var template = Handlebars.compile(source);
                                           var context = { pages: range(page,pageCount) };
                                           var html = template(context);
                                           var paginationTag = opts.postsDiv.parent().find(".pagination");
                                           paginationTag.length > 0 ? paginationTag.replaceWith(html) : opts.postsDiv.after(html);
                                           
                                           function changePage(page) {
                                               pageItems.removeClass('active');
                                               pageItems.filter('[data-page="' + page + '"]').addClass('active');
                                               loadPosts(data.slice(page * opts.pageMax - opts.pageMax, page * opts.pageMax));
                                               paginate(page,pageCount);
                                               
                                               if (page <= 1) {
                                    
                                                   hidePrev();
                                               }
                                               scrollTop();
                                               runAjax(page);
                                               
                                           }
                                    
                                           var pageItems = arasjQuery('.pagination>li.pagination-page');
                                           var pageItemsLastPage = arasjQuery('.pagination li').length - 2;
                                           pageItems.removeClass('active');
                                           pageItems.filter('[data-page="' + page + '"]').addClass('active');
                                    
                                           pageItems.on('click', function () {
                                               
                                               getDataPageNo = this.getAttribute('data-page')
                                               //console.log(getDataPageNo)
                                               changePage(getDataPageNo);
                                               
                                               if (getDataPageNo == 1) {
                                                   hidePrev();
                                               }
                                               else if (getDataPageNo == pageItemsLastPage) {
                                                   hideNext();
                                               }else {
                                                   showPrev();
                                                   showNext();
                                               }
                                           });
                                    
                                           arasjQuery('.pagination>li.pagination-prev').on('click', function () {
                                               gotoPageNumber = parseInt(arasjQuery('.pagination>li.active').attr('data-page')) - 1;
                                               changePage(gotoPageNumber);
                                               
                                           });
                                    
                                           arasjQuery('.pagination>li.pagination-next').on('click', function () {
                                               gotoPageNumber = parseInt(arasjQuery('.pagination>li.active').attr('data-page')) + 1;
                                               if (gotoPageNumber > pageCount) {
                                                   gotoPageNumber = 1;
                                                   showPrev();
                                               }
                                               changePage(gotoPageNumber);
                                               
                                           });
                                       }
                                       
                                       
                                       arasjQuery("#acfilter-form").submit(function(e) {
                                    
                                            runAjax(1);
                                           e.preventDefault(); 
                                       });
                                       
                                    
                                               
                                       function runAjax(pageNumber){
                                           hideCount();
                                           hidePosts();
                                           hidePagination();
                                           var keyWord = arasjQuery("#keyWord").val();
                                           var start_date = arasjQuery("#start_date").val();
                                           var end_date = arasjQuery("#end_date").val();
                                           var ac_category = arasjQuery("#ac_category").val();
                                           
                                           if (keyWord  === '' && start_date === '' && end_date === ''&& ac_category == null) {
                                               cosyAlert('لطفا حداقل یکی از گزینه های جستجو را انتخاب نمایید','error');
                                               showAlert();
                                               return false;
                                           }
                                           if(start_date && end_date && start_date>end_date){
                                               cosyAlert('تاریخ شروع باید کوچکتر مساوی تاریخ پایان باشد','error');
                                               return false;
                                           }
                                           hideAlert();
                                           if(start_date){
                                               start_date = setInputDate(start_date);
                                           }
                                           
                                           if(end_date){
                                               end_date = setInputDate(end_date);
                                           }
                                           
                                           arasjQuery.ajax({
                                               dataType: 'json',
                                               url: opts.dataUrl,
                                               type: 'POST',
                                               data: {
                                                     'keyWord': keyWord,
                                                     'end_date': end_date,
                                                     'start_date': start_date,
                                                     'category': ac_category,
                                                     'pageNumber': pageNumber,
                                                     'option': 'com_acfilter',
                                                     'view': 'ajax',
                                                     'tmpl':'component',
                                                     'format': 'filter'                                              
                                               },
                                               beforeSend: function(){
                                    
                                                   arasjQuery('#ajax-loading').show();
                                               },
                                               complete: function(){
                                                   arasjQuery('#ajax-loading').fadeIn(1000).hide(1000);
                                               },
                                               success: function (response_json) {
                                                   
                                                   if(response_json){
                                    
                                                       data = arasjQuery(response_json.records.page);
                                                       dataCount = response_json.recourd_count;
                                                       if(response_json.error){
                                                           cosyAlert(response_json.error,'error');
                                                           return false;
                                                       }
                                                       if(dataCount<=opts.pageMax){
                                                           hidePagination();
                                                       }
                                                       pageCount = Math.ceil(dataCount / opts.pageMax);
                                                       if (dataCount > opts.pageMax) {
                                                           paginate(pageNumber,pageCount);
                                                           posts = data.slice(0, opts.pageMax);
                                                       } else {
                                                           posts = data;
                                                       }
                                                       
                                                       loadPosts(posts);
                                                       arasjQuery("#display-count").show(800);
                                                       arasjQuery("#display-count").html("<p class='alert alert-info'> تعداد نتایج یافت شده با جستجوی شما: "+dataCount+"</p>");
                                                    
                                                       
                                                   }else{
                                                      cosyAlert('جستجوی شما بدون نتیجه بود','error');
                                                      arasjQuery("#posts").show();
                                                      arasjQuery("#posts").html("<p class='alert alert-danger'>جستجوی شما بدون نتیجه بود</p>"); 
                                                      arasjQuery(".pagination").hide(800); 
                                                   }
                                         
                                               }
                                           });
                                               
                                           
                                       }
                                       
                                          
                                           
                                    
                                    });
                                    
                                    
                                    
